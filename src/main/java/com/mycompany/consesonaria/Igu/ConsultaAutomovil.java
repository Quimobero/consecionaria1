    private void cargarTabla() {
        //hacemos que la tabla no sea editable
        
        DefaultTableModel modeloTabla = new DefaultTableModel(){
            @ Override
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        
        //poner titulos a las columnas
        String titulo[] = {"Id", "Modelo", "Marca", "Motor", "Color", "Patente", "Puertas"};
        
        modeloTabla.setColumnIdentifiers(titulo);
        
        //Traer los autos desde la base de datos
        
        List <automovil> listaAutomoviles = control.traerautos();
        
        //Set autos en la tabla
        
        if(listaAutomoviles != null){
            for (automovil auto : listaAutomoviles){
                Object[] objecto = {auto.getId(),auto.getModelo(),auto.getMarca(),auto.getMotor(),auto.getColor(), auto.getPatente(),auto.getCantPuerta()};
                
                modeloTabla.addRow(objecto);
            }
        }
        
        
        Tablaautos.setModel( modeloTabla);
        
        
        
    }
